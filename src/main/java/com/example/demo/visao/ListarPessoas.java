    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseClicked
        int i = this.jTable1.getSelectedRow();
        if (i >= 0) {
            DefaultTableModel defaultTableModel = (DefaultTableModel)this.jTable1.getModel();
            String nome = (String)defaultTableModel.getValueAt(i, 1);
            String cpf = (String)defaultTableModel.getValueAt(i, 2);
            String telefone = (String)defaultTableModel.getValueAt(i, 3);
            String email = (String)defaultTableModel.getValueAt(i, 4);
            String endereco = (String)defaultTableModel.getValueAt(i, 5);

            this.nameEdit.setText(nome.toString());
            this.cpfEdit.setText(cpf.toString());
            this.telefoneEdit.setText(telefone.toString());
            this.emailEdit.setText(email.toString());
            this.enderecoEdit.setText(endereco.toString());

        }
    }//GEN-LAST:event_jTable1MouseClicked

