    private void editarBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_editarBtnActionPerformed
       int i = this.jTable1.getSelectedRow();
        if (i >= 0) {
            Long id = (Long) ((DefaultTableModel) this.jTable1.getModel()).getValueAt(i, 0);
            Pessoa pessoa = this.lerCampos();
            pessoaController.atualizarPessoa(id, pessoa);
            this.atualizarTabela();
            this.limparCampos();
        }
    }//GEN-LAST:event_editarBtnActionPerformed

    public void limparCampos() {
        this.nameEdit.setText("");
        this.telefoneEdit.setText("");
        this.cpfEdit.setText("");
        this.emailEdit.setText("");
        this.enderecoEdit.setText("");
    }

    public Pessoa lerCampos() {
        Pessoa pessoa = new Pessoa();
        pessoa.setNome(this.nameEdit.getText());
        pessoa.setTelefone(this.telefoneEdit.getText());
        pessoa.setCpf(this.cpfEdit.getText());
        pessoa.setEmail(this.emailEdit.getText());
        pessoa.setEndereco(this.enderecoEdit.getText());
        return pessoa;
    }

    public void atualizarTabela() {
        ((DefaultTableModel)this.jTable1.getModel()).setNumRows(0);
        List<Pessoa> pessoas = this.pessoaController.listarPessoas();
        for (Pessoa pessoa : pessoas) {
            Object[] row = {
                pessoa.getId(),
                pessoa.getNome(),
                pessoa.getCpf(),
                pessoa.getTelefone(),
                pessoa.getEmail(),
                pessoa.getEndereco()
            };

            ((DefaultTableModel) this.jTable1.getModel()).addRow(row);
        }
    }
    private void adicionarBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_adicionarBtnActionPerformed
        Pessoa pessoa = this.lerCampos();
        this.pessoaController.criarPessoa(pessoa);
        this.atualizarTabela();
        this.limparCampos();
    }//GEN-LAST:event_adicionarBtnActionPerformed

    private void removerBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_removerBtnActionPerformed
        int i = this.jTable1.getSelectedRow();
        if (i >= 0) {
            Long id = (Long)((DefaultTableModel)this.jTable1.getModel()).getValueAt(i, 0);
            this.pessoaController.removerPessoa(id);
            ((DefaultTableModel)this.jTable1.getModel()).removeRow(i);
            this.limparCampos();
        }
    }//GEN-LAST:event_removerBtnActionPerformed

